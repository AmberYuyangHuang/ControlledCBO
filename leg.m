     case 0
      p=0.*x+1;
     case 1
      p=x;
     case 2
      p=(3.*x.^2)./2 - 1./2;
     case 3
      p=(5.*x.^3)./2 - (3.*x)./2;
     case 4
      p=(35.*x.^4)./8 - (15.*x.^2)./4 + 3./8;
     case 5
      p=(63.*x.^5)./8 - (35.*x.^3)./4 + (15.*x)./8;
     case 6
      p=(231.*x.^6)./16 - (315.*x.^4)./16 + (105.*x.^2)./16 - 5./16;
     case 7
      p=(429.*x.^7)./16 - (693.*x.^5)./16 + (315.*x.^3)./16 - (35.*x)./16;
     case 8
      p=(6435.*x.^8)./128 - (3003.*x.^6)./32 + (3465.*x.^4)./64 - (315.*x.^2)./32 + 35./128;
     case 9
      p=(12155.*x.^9)./128 - (6435.*x.^7)./32 + (9009.*x.^5)./64 - (1155.*x.^3)./32 + (315.*x)./128;
     case 10
      p=(46189.*x.^10)./256 - (109395.*x.^8)./256 + (45045.*x.^6)./128 - (15015.*x.^4)./128 + (3465.*x.^2)./256 - 63./256;
 
 end
